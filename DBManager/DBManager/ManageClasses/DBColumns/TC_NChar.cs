namespace DBManager.DBColumns
{
    /// <summary>
    /// Колонка БД с строковыми данными
    /// </summary>
    public sealed class DBC_NChar : DBColumn<System.Data.SqlTypes.SqlString, string>
    {
        internal DBC_NChar(int size, string name, bool nullable = false) : base(new DBColumnProperties(System.Data.SqlDbType.NVarChar
            , name: name
            , size: size
            , nullable: nullable))
        { }
    }
}
